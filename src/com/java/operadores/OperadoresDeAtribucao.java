package com.java.operadores;

public class OperadoresDeAtribucao {

    public static void main(String[] args) {
        // =, -=, +=, *=, /=, %=
        int salario = 1800;
        //Podemos abreviar essa linha
        salario = salario + 1800;
        //Fazendo da seguinte forma
        salario += 1800;

        System.out.println(salario);

    }
}
