package com.java.operadores;

public class OperadoresAritmeticos {
    public static void main(String[] args) {

        System.out.println(2 + 3);

        double x = 34.75;
        float y = (float) 34.76;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);

        int a = 9;
        int b = 3;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);

        //modulo é o resto da divisão
        System.out.println(a % b);

    }
}
